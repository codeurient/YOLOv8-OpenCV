# 1. Lane Segmentation (Yol Qüvvəsi Seqmentasiyası):
# Lane segmentation, bir görüntüdə və ya videoda yolun sürət zolaqlarını (lane) müəyyənləşdirmək və ayırmaq prosesidir. Bu, avtonom nəqliyyat vasitələrinin 
# yol boyunca doğru bir şəkildə hərəkət etməsini təmin etmək üçün həyati əhəmiyyət kəsb edir.

# Nə üçün lazımdır?
#     Avtonom sürücülük sistemləri üçün yol zolaqlarının dəqiq təhlili və təyini vacibdir, çünki nəqliyyat vasitəsinin yolda düzgün qalması və digər nəqliyyat 
#     vasitələrindən təhlükəsiz məsafədə olması təmin edilir.
#     Yol qəzalarının qarşısını almaq və hərəkət təhlükəsizliyini artırmaq üçün istifadə olunur.

# İstifadə sahələri: Avtonom sürücülük və şəhər içi nəqliyyat.

# --------------------------------------------------------------------------------------------------------------------------------------------------------------
# --------------------------------------------------------------------------------------------------------------------------------------------------------------

# 2. Road Surface Segmentation (Yol Səthi Seqmentasiyası):
# Road surface segmentation, bir görüntüdə yol səthinin vəziyyətini və xüsusiyyətlərini müəyyənləşdirmək üçün istifadə edilir. Bu proses yolun səthindəki defektləri, 
# təhqiqatları və zərərli elementləri (daş, çuxurlar, buz və s.) ayırd etmək məqsədini güdür.

# Nə üçün lazımdır?
#     Yol səthinin keyfiyyətini və təhlükəsizliyini qiymətləndirmək üçün vacibdir.
#     Avtonom nəqliyyat vasitələrinin yolların vəziyyətinə uyğun hərəkət etməsini təmin etmək və təhlükəsizlik problemlərini aradan qaldırmaq üçün lazımdır.

# İstifadə sahələri:
#     Avtomatik xəstəlik analizi və yol təmir sistemləri.
#     Avtomobil sistemləri (sükan və sürət sistemi) yol səthinin vəziyyətinə uyğunlaşmaq üçün.

# --------------------------------------------------------------------------------------------------------------------------------------------------------------
# --------------------------------------------------------------------------------------------------------------------------------------------------------------

# 3. Traffic Light Detection (Trafik İşığı Tanıma):
# Traffic light detection, bir görüntüdə və ya videoda trafik işıqlarını (qırmızı, yaşıl, sarı) və onların vəziyyətini (yanıb-yanmadığını) müəyyənləşdirmək prosesidir. 
# Bu, nəqliyyat vasitələrinin düzgün hərəkət etməsi və trafik qaydalarına riayət etməsi üçün çox vacibdir.

# Nə üçün lazımdır?
# Avtonom nəqliyyat vasitələri üçün təhlükəsiz və düzgün qərar qəbul etməyi təmin edir. Nəqliyyat vasitəsinin trafik işıqlarını doğru tanıyıb müvafiq olaraq dayanması 
# və ya hərəkət etməsi lazımdır.
# İnsan sürücüləri üçün də trafik idarəetməsini asanlaşdırır.

# İstifadə sahələri:
# Avtonom nəqliyyat sistemləri və robot sürücülər.
# Yol təhlükəsizliyi monitorinqi və trafik idarəetməsi.

# --------------------------------------------------------------------------------------------------------------------------------------------------------------
# --------------------------------------------------------------------------------------------------------------------------------------------------------------

# 4. Traffic Sign Detection (Trafik Nişanı Tanıma):
# Traffic sign detection, yol üzərindəki trafik nişanlarını (sürət həddi, dayanma, əyləc, və s.) tapmaq və tanımaq məqsədini güdür. Bu, nəqliyyat vasitələrinə yol 
# nişanlarını düzgün başa düşmək və müvafiq hərəkət etmək üçün vacibdir.

# Nə üçün lazımdır?
# Trafik nişanlarını doğru şəkildə təhlil edib tətbiq etmək, avtonom nəqliyyat vasitələrinin müvafiq sürətə uyğun hərəkət etməsini təmin edir.
# Yol təhlükəsizliyi və təhlükəli ərazilərin tanınması üçün vacibdir.

# İstifadə sahələri:
# Avtomatik sürücülük sistemləri və avtonom nəqliyyat vasitələri.
# Trafik monitorinqi və yol nişanı təhlili.

# --------------------------------------------------------------------------------------------------------------------------------------------------------------
# --------------------------------------------------------------------------------------------------------------------------------------------------------------

# 5. Person & Car Detection (Şəxs və Maşın Tanıma):
# Person & car detection, bir görüntü və ya videoda şəxsləri və nəqliyyat vasitələrini (maşın, avtobus, velosiped və s.) tapmaq və tanımaq prosesidir. Bu vəzifə, 
# müxtəlif nəqliyyat vasitələri və insanları tanımaq və izləmək üçün istifadə olunur.

# Nə üçün lazımdır?
# Avtonom nəqliyyat vasitələrinin insanları və digər nəqliyyat vasitələrini izləməsi və qarşısını almaq üçün istifadə olunur.
# Şəhər təhlükəsizliyi və trafik monitorinqi sahələrində əhəmiyyətlidir, çünki hərəkət edən şəxslərin və nəqliyyat vasitələrinin yerini bilmək, qəza və hadisələrin 
# qarşısını almağa kömək edir.

# İstifadə sahələri:
# Avtonom nəqliyyat vasitələri üçün insan və maşın tanıma.
# Yol təhlükəsizliyi monitorinqi və avtomatik təhlükə təhlili.

