# Image Processing (Şəkil Emalı):
# Şəkil emalı, rəqəmsal şəkilləri və videoları emal etmək və təhlil etmək üçün tətbiq olunan müxtəlif metodlardan ibarətdir. 
# Məqsəd, görüntülərin keyfiyyətini yaxşılaşdırmaq, şəkil üzərində müxtəlif dəyişikliklər etmək və şəkilləri hazırlamaqdır. 
# Bu proseslər əsasən şəkil üzərində əməliyyatlar yerinə yetirməkdən ibarətdir, məsələn:

# Filtrləmə (görüntü üzərində müxtəlif filtrlər tətbiq etmək).
# Şəkil ölçüsünü dəyişdirmək (yuxarı və ya aşağı ölçü dəyişdirmək).
# Şəkil tündləşdirmə və ya işıqlama.
# Həssaslıq (contrast) tənzimləmə.
# Şəkil üzərində kənarları tapmaq (edge detection).
# Şəkil üzərində müxtəlif dəyişikliklər etmək (boz rəngə çevirmək, müəyyən detalları çıxarmaq və s.).







# Computer Vision (Kompüter Görməsi):
# Kompüter görməsi, şəkil və video məlumatlarından mənalı informasiya çıxarmaq və bu məlumatları təhlil etmək üçün istifadə olunan 
# daha geniş və mürəkkəb sahədir. Məqsəd, kompüterlərin insan gözü kimi şəkil və video məlumatlarını təhlil edib, obyektləri tanıması, 
# analiz etməsi və qərar verməsidir. Kompüter görməsi, şəkil və video məlumatlarından daha yüksək səviyyəli nəticələr çıxarmaq üçün 
# istifadə olunur, məsələn:

# Obyekt tanıma: Şəkildə və ya videoda obyektləri tanımaq və etiketləmək (məsələn, maşın, insan, heyvan və s.).
# Üz tanıma: İnsan üzlərini tanımaq və onlara uyğun əməliyyatlar yerinə yetirmək.
# Hərəkət izləmə: Video görüntülərində hərəkət edən obyektləri izləmək.
# Təsnifat: Şəkil və ya video kateqoriyalarına ayırmaq (məsələn, şəkilin nə olduğunu müəyyənləşdirmək).
# Üç ölçülü (3D) analizlər: Görüntülərdən obyektin və ya səhnənin 3D modelini qurmaq.
# Məkan və dərinlik təhlili: Görüntülərdə obyektlərin dərinliyini və yerləşməsini təhlil etmək.